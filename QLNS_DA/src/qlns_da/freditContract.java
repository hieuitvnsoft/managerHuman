    private void loadContract(String id) throws SQLException {
        MannagerContracts mn = new MannagerContracts();
        Contracts ct = mn.getContract(id);
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        txtContent.setText(ct.getContenContract());
        txtDateExp.setText(df.format(ct.getDateExpied()));
        txtDateSign.setText(df.format(ct.getDateSign()));
        txtID.setText(ct.getId());
        if (ct.getStatusContract() == 1) {
            cboStatus.setSelectedIndex(0);
        } else {
            cboStatus.setSelectedIndex(1);
        }
    }
