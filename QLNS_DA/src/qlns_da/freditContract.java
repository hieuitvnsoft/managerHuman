    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            Contracts ct = new Contracts();
            ct.setId(txtID.getText());
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");

            Date date = df.parse(txtDateExp.getText());
            Date date1 = df.parse(txtDateSign.getText());
            ct.setDateSign(date);
            ct.setDateExpied(date1);
            StatusContract sc = (StatusContract) cboStatus.getSelectedItem();
            int stt = sc.getId();
            ct.setStatusContract(stt);
            ct.setContenContract(txtContent.getText());

            MannagerContracts mn = new MannagerContracts();
            mn.updateContract(ct);
        } catch (SQLException ex) {
            Logger.getLogger(freditContract.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(freditContract.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAddActionPerformed

