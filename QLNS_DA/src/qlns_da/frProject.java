    private void loadPro() throws SQLException {
        ManagerPro mn = new ManagerPro();
        Vector<Project> ls = new Vector<>();
        ls = mn.getPro();
        for (int i = 0; i < ls.size(); i++) {
            Project elementAt = ls.elementAt(i);
            Vector row = new Vector<>();
            row.add(elementAt.getId());
            row.add(elementAt.getNamePro());
            row.add(elementAt.getContentPro());
            row.add(elementAt.getDateSign());
            row.add(elementAt.getDateDeadline());
            row.add(String.valueOf(elementAt.getValPro()));
            dtm.addRow(row);
        }

    }
