    private void tblProjectMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProjectMouseReleased
        if (evt.isPopupTrigger()) {
            jPopupMenu1.show(evt.getComponent(), evt.getX(), evt.getY());
            int row = evt.getY() / tblProject.getRowHeight();
            if (row > -1 && row < tblProject.getColumnCount()) {
                tblProject.setRowSelectionInterval(row, row);

            } else {
                tblProject.clearSelection();
            }
        }
    }//GEN-LAST:event_tblProjectMouseReleased

