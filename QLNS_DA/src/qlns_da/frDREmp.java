    private void cboDepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboDepItemStateChanged
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        try {
            loadEmp();
            loadEmpcb();
        } catch (SQLException ex) {
            Logger.getLogger(frDREmp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cboDepItemStateChanged

    private void cboIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboIDItemStateChanged
        Empxx ep = (Empxx) cboID.getSelectedItem();

        txtName.setText(ep.getName());
        txtCV.setText(ep.getCv());
    }//GEN-LAST:event_cboIDItemStateChanged

    private void tblEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpMouseClicked
        int r = tblEmp.getSelectedRow();
        String id = (String) tblEmp.getValueAt(r, 0);

        ComboBoxModel<Empxx> pv = cboID.getModel();
        for (int j = 0; j < cboID.getItemCount(); j++) {
            String n = pv.getElementAt(j).getId();
            if (n.equalsIgnoreCase(id)) {
                cboID.setSelectedIndex(j);
            }
        }
        txtName.setText((String) tblEmp.getValueAt(r, 1));
        txtCV.setText((String) tblEmp.getValueAt(r, 2));

    }//GEN-LAST:event_tblEmpMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            // TODO add your handling code here:
            DREmp drEmp = new DREmp();

            DisciplineReward dr = (DisciplineReward) cboDR.getSelectedItem();
            drEmp.setIdDR(dr.getId());

            Empxx empx = (Empxx) cboID.getSelectedItem();
            drEmp.setIdEmp(empx.getId());
            drEmp.setDateDR(txtDate.getDate());
            drEmp.setReson(txtReson.getText());
            drEmp.setContent(txtContent.getText());
            ManagerDisciplineReward mn = new ManagerDisciplineReward();
            mn.InsertDREmp(drEmp);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi thêm mới xin kiểm tra lai", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed


