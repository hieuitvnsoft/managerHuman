    private void loadDR() throws SQLException {
        ManagerDisciplineReward mn = new ManagerDisciplineReward();
        Vector<DisciplineReward> ls = mn.getAllDisRe();
        dcbm1 = new DefaultComboBoxModel(ls);
        cboDR.setModel(dcbm1);
    }

    private void loadDepar() throws SQLException {
        ManagerDeparment mnDepar = new ManagerDeparment();
        Vector<Deparment> lstDepar = mnDepar.getAllDeparment();
        dcbm2 = new DefaultComboBoxModel<>(lstDepar);
        cboDep.setModel(dcbm2);

    }

    private void loadEmpcb() throws SQLException {
        ManagerDisciplineReward mn = new ManagerDisciplineReward();
        Deparment dp = (Deparment) cboDep.getSelectedItem();
        String id = dp.getIdDepartment();
        Vector<Empxx> lstDepar = mn.getEmp(id);
        dcbm3 = new DefaultComboBoxModel<>(lstDepar);
        cboID.setModel(dcbm3);

        txtName.setText(lstDepar.firstElement().getName());
        txtCV.setText(lstDepar.firstElement().getCv());

    }

    private void loadEmp() throws SQLException {

        Deparment dp = (Deparment) cboDep.getSelectedItem();
        String id = dp.getIdDepartment();
        ManagerDisciplineReward mn = new ManagerDisciplineReward();
        Vector<Empxx> lstDepar = mn.getEmp(id);
        for (int i = 0; i < lstDepar.size(); i++) {
            Empxx elementAt = lstDepar.elementAt(i);
            Vector row = new Vector();
            row.add(elementAt.getId());
            row.add(elementAt.getName());
            row.add(elementAt.getCv());
            dtm.addRow(row);
        }

    }

