    private void loadDep() throws SQLException {
        ManagerDep mnd = new ManagerDep();
        Vector<ManagerDep.iforDep> ls = new Vector<>();

        ls = mnd.getDep();
        for (int i = 0; i < ls.size(); i++) {
            ManagerDep.iforDep elementAt = ls.elementAt(i);
            Vector<String> k = new Vector<>();
            k.add(elementAt.getId());
            k.add(elementAt.getName());
            k.add(elementAt.getPhone());
            k.add(elementAt.getTp());
            k.add(elementAt.getPp());
            dtm.addRow(k);
        }

    }

