    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            int r = tblListDep.getSelectedRow();
            String id = (String) tblListDep.getValueAt(r, 0);
            ManagerDep mn = new ManagerDep();
            mn.deleteDp(id);
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            loadDep();
        } catch (SQLException ex) {
            Logger.getLogger(frDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

